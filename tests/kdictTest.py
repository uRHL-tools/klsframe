import klsframe.protypes.kdicts as _kdicts


def test_sort_dict():
    dicto = {'1': 'shit', '2': 'here', '3': 'we', '4': 'go', '5': 'again'}
    print(f"Alphabetical Key order:\n{_kdicts.sort_dict(dicto)}")
    print(f"Alphabetical inverse Key order:\n{_kdicts.sort_dict(dicto, reverse=True)}")
    print(f"Alphabetical value order:\n{_kdicts.sort_dict(dicto, sortby='values')}")
    print(f"Alphabetical inverse value order:\n{_kdicts.sort_dict(dicto, sortby='values', reverse=True)}")


def test_dict_to_table():
    a = {'Nombre': 'pedro', 'Apellidos': 'perez', 'Edad': 12, 'hobbies': ['skateboarding']}
